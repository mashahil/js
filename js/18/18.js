var per=prompt("What is your percentage of mark?");
per=parseInt(per);

if((per>=80)&&(per<=100))
{

			
			alert("You got Grade is A");
		
}
else if((per>=60)&&(per<=79))
{

			
			alert("You got Grade is B");
		
}
else if((per>=40)&&(per<=59))
{

			
			alert("You got Grade is C");
		
}
else if((per>=30)&&(per<=39))
{

			
			alert("You got Grade is D");
		
}
else if((per>=10)&&(per<=29))
{

			
			alert("You got Grade is E");
		
}
else{
      alert("You got Grade is E-");
}
